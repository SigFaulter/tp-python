def concatenate(str1, str2):
    """
    Concatene deux chaines de caracteres.

    Args:
        str1 (str): La premiere chaine de caracteres.
        str2 (str): La deuxieme chaine de caracteres.

    Returns:
        str: La concatenation des deux chaines de caracteres.
    """
    return str1 + str2

def to_uppercase(string):
    """
    Convertit une chaine de caracteres en majuscules.

    Args:
        string (str): La chaine de caracteres a convertir.

    Returns:
        str: La chaine de caracteres en majuscules.
    """
    return string.upper()

